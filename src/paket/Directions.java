package paket;

/**
 * This is an Enum class that contains the possible 4 directions for a paket.car.
 */
public enum Directions {
    /**
     * Enum for when the paket.car is facing UP ( + y if moving)
     */
    UP,
    /**
     * Enum for when the paket.car is facing LEFT ( - x if moving)
     */
    LEFT,
    /**
     * Enum for when the paket.car is facing RIGHT ( + x if moving)
     */
    RIGHT,
    /**
     * Enum for when the paket.car is facing DOWN ( - y if moving)
     */
    DOWN,
}
